func calculateRectangleIntersectArea(input: [(id: Int, x: Int, y: Int, width: Int, height: Int)]) -> Int {
    var material: [String:Int] = [:]
    for i in input {
        Array(i.x ... i.x + i.width - 1).forEach { (x) in
            Array(i.y ... i.y + i.height - 1).forEach { (y) in
                material["\(x):\(y)"] = ( material["\(x):\(y)"] ?? 0 ) + 1
            }
        }
    }
    
    let intersects = material.reduce (0, { total, pair in pair.value > 1 ? total + 1 : total })
    
    return intersects
}

